public class Security {
    private String name;
    private String id;
    //Setter
    public void setName(String newName){         //encapsulation use
        this.name=newName;

    }
    public void setId(String newId){
        this.id=newId;

    }
    //Getter
    public String getName() {
        return name;
    }
    public String getId() {
        return id;
    }
}





